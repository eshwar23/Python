# 1st - first form of for loop with initial value , final value (not included) and step value
'''
for i in range(1,11,1):
    print (i)
'''


# 2nd - second form of for loop with initial value and final value , by default step value is 1
'''
for i in range(1,11):
    print (i)
'''


# 3rd - third form of for loop with only final value , default start value is 1 and default step value is 1
'''
for i in range(11):
    print (i)
'''


